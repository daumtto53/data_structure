/*
	https://www.acmicpc.net/problem/9095
	틀렸던 이유 : 입력으로 1,2,3 이 들어오는 경우를 따로 예외처리를 하여 각각 0,1,3으로 계산했음.
 */
#include <iostream>

using namespace std;

int		memo[12] = {0, };

int		num_of_cases(int N)
{
	if (memo[N] != 0)
		return (memo[N]);
	for (int i = 1; i <= 3; i++)
		memo[N] += num_of_cases(N - i);
	return (memo[N]);
}

int		main()
{
	int		tc;
	int		n;

	cin >> tc;

	memo[1] = 1;
	memo[2] = 2;
	memo[3] = 4;

	while (tc--)
	{
		cin >> n;
		cout << num_of_cases(n);
		cout << endl;
	}
	return (0);
}
