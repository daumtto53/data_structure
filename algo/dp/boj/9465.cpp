#include <iostream>
#include <algorithm>

int		memo[2][100001];
int		weight[2][100001];

int		sticker(int n)
{
	int		i, j;

	memo[0][0] = weight[0][0];
	memo[1][0] = weight[1][0];
	memo[0][1] = weight[1][0] + weight[0][1];
	memo[1][1] = weight[0][0] + weight[1][1];

	for (j = 2; j < n; j++)
	{
		memo[0][j] = std::max( memo[1][j-1], memo[1][j-2] ) + weight[0][j];
		memo[1][j] = std::max( memo[0][j-1], memo[0][j-2] ) + weight[1][j];
	}
	return (std::max(memo[0][n-1], memo[1][n-1]));
}

using namespace std;

int		main()
{
	int		tc, n;

	cin >> tc;

	while (tc--)
	{
		cin >> n;
		for (int i = 0; i < n; i++)
			cin >> weight[0][i];
		for (int i = 0; i < n; i++)
			cin >> weight[1][i];
		cout << sticker(n) << endl;
	}
	// cin >> max;
}
